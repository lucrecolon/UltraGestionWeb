<ul type="disc">
<li><font color="#993333"><b>Plan de Cuentas</b></font>
<br>
<br>
<ul type="circle">
<li>Codificación adaptable por el Usuario (10 caracteres y agrupamientos de hasta cinco niveles).  
<li>Las cuentas contables pueden asociarse o no a los distintos Centros de Costos e identificarse por atributos funcionales.
<li>Definición de cuentas imputables y no imputables por el usuario.
<li>Generación automática de los asientos de cierre, apertura y ajuste por inflación.
<li>Generación automática de asientos de traslación (diferencias de cambio al cierre) en moneda local y extranjera.
<li>Anulación,  Reversión y Repetición de Asientos.
<li>No es necesario cerrar un ejercicio para iniciar el siguiente.
<li>Renumeración de Asientos por período en la opción de Diario General.
</ul>
<br>
<br>
<li><font color="#993333"><b>Carga de Asientos</b></font>
<br>
<br>
<ul type="circle">
<li>Numeración correlativa mensual o anual de asientos.
<li>Ingreso de asientos tipo predefinidos (utilizando o no Centro de Costos).
<li>Eliminación Múltiple de asientos.
<li>Listado Múltiple de asientos no consecutivos.
<li>Funciones de Seguridad en la Carga de asientos limitando su uso a Usuarios calificados.
<li>Reversión de Asientos.
<li>Control de Origen del asiento para habilitar o no su eliminación o modificación.
<li>Limitación de Carga por Mes o Cuenta Cerrada.
</ul>
<br>
<br>
<li><font color="#993333"><b>Ajustes por Inflación</b></font>
<br>
<br>
<ul type="circle">
<li>Procedimiento de Ajuste de acuerdo a las normas profesionales.
<li>Alta y Mantenimiento de Coeficientes por cada mes del ejercicio .
<li>Definición del código de ajuste correspondiente a cada cuenta.
<li>Imputación del resultado por exposición a la inflación en distintas cuentas.
<li>Los Asientos se ajustan por la Fecha del Asiento.
<li>Informe Reporte del ajuste y selección a voluntad de la generación del asiento resultante.
</ul>
<br>
<br>
<li><font color="#993333"><b>Información Contable y De Gestión</b></font>
<br>
<br>
<ul type="circle">
<li>Reportes de Plan de Cuentas, Diario General, Mayores, Balance de Sumas y Saldos, y Balance General (ajustado/histórico) de un periodo dado o acumulado del ejercicio.
<li>Generación de Reportes con agrupamientos por cuentas y / o centro de costos.
<li>Información cronológica y analítica por Centros de Costos.
<li>Información cronológica y analítica por Áreas de Negocio.
</ul>
<br>
<br>
<li><font color="#993333"><b>Cobranzas</b></font>
<br>
<br>
<ul type="circle">
<li>Emisión de las Cobranzas actualizando las cuentas corrientes aplicando documentos registrados en Saldos a Cobrar  de la Cuenta y otorgando Anticipos para futuros. 
<li>Cálculo de Diferencias de Cambio.
<li>Aplicación de Retenciones Sufridas  IVA, Ingresos Brutos y generación soporte magnético AFIP.
<li>Ingresos de fondos directos o de terceros.
<li>Control de Numeración de Recibos activos.
</ul>
<br>
<br>
<li><font color="#993333"><b>Ordenes de Pago</b></font>
<br>
<br>
<ul type="circle">
<li>Emisión de las Ordenes de Pago actualizando las cuentas corrientes aplicando documentos registrados en Saldos A pagar de la Cuenta y otorgando Anticipos para futuros.
<li>Cálculo de Diferencias de Cambio.
<li>Emisión certificados de  retenciones IVA, Ingresos Brutos y generación soporte magnético AFIP.
<li>Egresos de fondos directos (efectivo, cheques propios al día o diferidos) o de terceros (endoso de valores recibidos).
<li>Control de chequeras activas.
</ul>
<br>
<br>
<li><font color="#993333"><b>Tesorería Movimientos de fondos</b></font>
<br>
<br>
<ul type="circle">
<li>Ingreso y canje de valores (cheques, tarjetas y / o documentos) recibidos de terceros.
<li>Administración de Cupones de Tarjetas de Crédito y su proyección de cobranzas.
<li>Depósitos  y extracciones bancarias.
<li>Información de datos de los valores recibidos y seguimiento hasta su depósito o entrega.
<li>Ingreso y liquidación de Fondos fijos.
<li>Registración de los movimientos financieros: efectivo, cheques propios, valores a depositar y cupones de tarjetas de crédito.
<li>Reporte Subdiario de movimiento de fondos, mayores de cuenta.
<li>Información sobre valores: Proyecciones de cobranzas, fechas de vencimiento, origen, destino, etc.
<li>Reportes de disponibilidad en bancos y Vencimientos futuros de Valores de Pago diferido por fecha y banco.
<li>Aplicación al banco de los Valores de Pago Diferido al Vencimiento.
<li>Reporte entre fechas de Ingresos por Cobranzas analíticos o resumen.
<li>Reporte entre fechas de Egresos por Pagos a Proveedores analíticos o resumen.
<li>Gestión para cálculo de comisiones por cobranzas.
<br>
