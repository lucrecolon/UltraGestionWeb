<ul type="disc">
<li><font color="#993333"><b>Artículos</b></font>
<br>
<br>
<ul type="circle">
<li>Libre codificación (hasta 13 caracteres).
<li>Agrupación de artículos, por Rubros, Familias o sus representativos para la industria del libro, Editor, Tema. Colección, Proveedor, etc.
<li>Identificación de la obra por Título, Título extendido, Autor e I.S.B.N.
<li>Accesorios de la obra (Cd, Videos, Diskettes, Posters, Revistas).
<li>Datos bibliográficos, Edición, Año, Encuadernación, Páginas, Novedad mes y año.
<li>Código de barras e imágenes para los artículos.
<li>Código de relación complementarios (sitio web, código de abastecimiento del proveedor).
<li>Doble unidad de medida, opcional por artículo.
<li>Costos locales y o por país de origen.
<li>Precios de Venta vigentes e históricos (mercado local y exterior).
<li>Definición de categorización del producto, prioridad de reposición y Descuentos Máximos.
<li>Alícuotas de impuestos (IVA, Impuestos al Video, Otros conceptos provinciales o municipales.
<li>Altas y actualización de precios desde el portal <a href="http://www.librosar.com.ar" target="about:blank">librosar.com.ar</a> (sitio oficicial de la Camara Argentina del libro)
</ul>
<br>
<br>
<li><font color="#993333"><b>Precios</b></font>
<br>
<br>
<ul type="circle">
<li>Listas de precios con distintos ordenamientos.
<li>Listas de Precios por Cliente.
<li>Actualización de listas en forma parcial o total, manual o automática, por porcentajes, margen sobre compras o importes.
<li>Actualización de Listas de precios desde archivo excel proporcionado por el Proveedor.
<li>Actualización de precios desde sitio de Cuspide Computación.
</ul>
<br>
<br>
<li><font color="#993333"><b>Pedidos al Proveedor</b></font>
<br>
<br>
<ul type="circle">
<li>Generación automática del pedido al proveedor partiendo de las ventas contemplando todos los modos de comercialización (firme, consignación, refuerzo, reposición, etc.) mediante una amigable interfase podrá alterar o ratificar a voluntad los items generados automaticamente y el modo de comercialización deseado. 
<li>Generación automática de pedidos partiendo del pedido del cliente o seña de venta. 
<li>Reportes pendientes por Pedido o Proveedor con diversas clasificaciones o filtros.
</ul>
<br>
<br>
<li><font color="#993333"><b>Inventarios Recepción</b></font>
<br>
<br>
<ul type="circle">
<li>Ingreso de recepción de artículos manual.
<li>Discriminación del Ingreso por modo de Comercialización, En Firme, Consignada o Con fecha de devolución.
<li>Ingreso de recepción automático partiendo de un Pedido de Suministro al Proveedor, el proceso reconocerá automáticamente el destino de la mercadería ingresada pedido de un cliente, reposición por ventas, solicitud por seña.
<li>Múltiples depósitos y control de reserva de mercadería.
<li>Emisión del Informe de recepción.
<li>Al recibir un Pedido de Suministro, permite ingresar una cantidad mayor al pendiente de recepción O ingresar una menor y dar por cumplido el Pedido.
<li>Despacho/Partida o Número de Serie por depósito.
</ul>
<br>
<br>
<li><font color="#993333"><b>Movimientos de Stock</b></font>
<br>
<br>
<ul type="circle">
<li>Generación de entradas, salidas y transferencias entre depósitos.
<li>Consultas de movimientos por fecha, artículo, artículo-depósito y depósito-árticulo, etc.
</ul>
<br>
<br>
<li><font color="#993333"><b>Inventarios Reportes</b></font>
<br>
<br>
<ul type="circle">
<li>Informe de Consignación Recibida de Proveedores por múltiples clasificaciones y filtros.
<li>Consulta Histórica Proveedor Artículo.
<li>Valorización de stock a precio promedio ponderando, precio de última compra, precio de reposición, precio de última compra actualizada o a cualquiera de las listas de precios.
<li>Stock mínimo, máximo, pendiente de recepción (por órdenes de compra generadas), comprometido (por los pedidos pendientes de Clientes).
<li>Módulo opcional de Toma de Inventario.
</ul>
<br>
<br>
<li><font color="#993333"><b>Facturación</b></font>
<br>
<br>
<ul type="circle">
<li>Contempla la emisión por todos los medios fiscales habilitados, Impresoras, o tickeadoras procesa según comportamiento seteado por el Supervisor habilitando o restringiendo el ingreso manual de Importes unitarios, descuentos, bonificaciones y permiso por stock negativo.
<li>Procesa  Facturas, Débitos, Créditos y Tickets como comprobantes fiscales habilitados. Permite en un mismo documento aceptar devoluciones, tomar señas y pedidos.
<li>Determina código de artículo a facturar asistido por lector de barra o panel de busqueda por múltiples claves (Título, ISBN, Autor, Editor, Palabra del título, etc.).
<li>Determina y calcula Impuestos Municipales, Provinciales y Nacionales según la situación fiscal del contribuyente, múltiples alícuotas de IVA en un mismo documento, y múltiples medios de pagos simultaneos identificando en detalle diario de caja cada medio utilizado tarjeta de crédito o débito, efectivos múltiples monedas y Valores.
<li>Captura y almacena Datos del Cliente para posterior explotación de mailing y servicios de fidelidad.
</ul>
<br>
<br>
<li><font color="#993333"><b>Reportes</b></font>
<br>
<br>
<ul type="circle">
<li>Genera informes en formato copiativo sustitutivo del libro IVA ventas y compras de cada período fiscal.
<li>Resumen de Venta y Recaudación entre fecha y hora, proporcionando Informe consolidado, por punto de venta o por Cajero.
<li>Ranking de Ventas por productos.
<li>Comisiones por vendedor.
<li>Estadísticas de Ventas entre fechas dadas proporcionando cantidades, importes promedios y totales. Informe con selección por múltiples filtros, título, editor, proveedor y colección.
<li>Rentabilidad y Costo de venta.
</ul>
