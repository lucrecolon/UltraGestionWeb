  <li><font color="#993333"><b>Seguridad</b></font>
  <br>
  <br>
  <ul type="circle">
    <li>Figura Usuario Supervisor, responsable de la administración de la población de Usuarios y parametrización del sistema.
    <li>Definición de usuarios con sus claves de acceso (password), asignaciones de perfiles por cada uno, categorización, fecha de expiración, inhabilitación temporaria, y creación de menús con permisos y restricciones.
    <li>Log de tránsito de información y manipulación de datos.
    <li>Transacciones identificadas por Operador, Fecha y Hora.
    <li>Restriciones en la generación y anulación de Comprobantes, Asientos y Documentos Fiscales a Usuarios no habilitados.
  </ul>
  <br>
  <br>
  <li><font color="#993333"><b>Funcionalidades</b></font>
  <br>
  <br>
  <ul type="circle">
    <li>Multi Empresa.
    <li>Interfase de uso asistida y homogenea en toda la aplicación.
    <li>Totalmente Bimonetario.
    <li>Todos los reportes pueden ser exportados directamente a otros formatos de datos para luego procesarlos por Hojas de Cálculo, Base de Datos o Procesadores de Texto.
    <li>Los listados pueden visualizarse previamente antes de ser impresos. Usted podrá consultar el mismo visualizando parcial o totalmente el informe, acceder a la primera hoja, la última o cualquier otra de su interès, imprimir a partir de una hoja indicada o todo el reporte y asignarle la cantidad de copias a voluntad.
    <li>Los reportes pueden contener a voluntad y seteados previamente imágenes de logos, fecha real de generaciòn y operador responsable del mismo.
    <li>Definicion de Regla de Negocios: Por un panel exclusivo de configuración podrá definir para
        cada transacción determinada reglas que permitan controlar la información que entra al sistema,
        definir mensajes al operador, activar controles y restricciones y en los casos necesarios solicitud
        de passwords autorizantes.
  </ul>
</ul>
